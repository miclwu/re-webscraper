FIELDNAMES = ["name", "url", "status", "checksum"]

class Status(Enum):
    OPEN = "Open"
    CLOSED = "Closed"
    CHECK = "Check Required"

DELIM = ";;"

DATABASE = "webscraper.db"
FUNDS_TABLE = "funds"
INFILE = "input.csv"
OUTFILE = "checkfunds.csv"

